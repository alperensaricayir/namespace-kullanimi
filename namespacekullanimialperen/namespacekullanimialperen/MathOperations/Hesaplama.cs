// MathOperations namespace içinde Hesaplama sınıfı tanımlandı
namespace MathOperations
{
    // Hesaplama sınıfı
    public class Hesaplama
    {
        // İki sayıyı toplar
        public int Topla(int sayi1, int sayi2)
        {
            return sayi1 + sayi2;
        }

        // İki sayıyı çarpar
        public int Carp(int sayi1, int sayi2)
        {
            return sayi1 * sayi2;
        }
    }
}